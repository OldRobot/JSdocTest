
/**
 * This function will calculate the area of a rectangle
 * @param {number} l - Length of the Rectangle
 * @param {number} w - Width of the rectangle
 * @returns Will retun the area.
 */
function areaOfRectangle(l,w){
    return l*w;
}


/**
 * This function caculates the area of a circle
 * @param {number} r - The radius of the circle
 * @returns Will return the area of the circle to 6 dp.
 */
function areaOfCircle(r){
    return 3.14159245 * r * r;
}

/**
 * Calculates the area of a tringle
 * @param {number} b base of the triangle
 * @param {number} h the perpendicular height
 * @returns the area of the triangle
 */
function areaOfTriangle(b,h){
    return 0.5*b * h;
}
